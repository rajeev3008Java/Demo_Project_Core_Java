package variables;

public class VariableRevisions {

    public static void main(String[]args){

        //SYNTAX ; Datatype varName;
        String name;

        //varName=value;
        name="JAVA";
        System.out.println("Printing variable name ;"+name);

        int age;
        age=38;
        System.out.println("Printing age :"+age);

        double salary;
        salary=98765.56;
        System.out.println("Printing salary :"+salary);

        String cityName;
        cityName="Pune";
        System.out.println("Printing cityName :"+cityName);

    }


}
